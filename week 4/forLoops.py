# Make a for loop that counts to 10
# range(start, stop, step)
for i in range(0, 10, 1):
	print(i+1)

print("--------------")

# For loop with only stop
for i in range(10):
	print(i+1)

print("--------------")

# Counting down from 10 to 1
for i in range(10, 0, -1):
	print(i)

print("--------------")

for i in range(-1, 11):
	print(i+1)

print("--------------")

# counting by steps greater than 1
for i in range(0, 10, 2):
	print(i)
